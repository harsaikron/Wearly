# ── Conversation format ────────────────────────────────────────────────────────

SYSTEM_PROMPT = """You are Wearly's clothing classifier AI, fine-tuned to accurately identify clothing items from photos.
For every image, return ONLY valid JSON with these exact fields:
- suggested_name: concise item name (e.g. "Navy Slim Chinos")
- category: one of shirt|formal_shirt|tshirt|pants|jeans|shorts|shoes|sneakers|loafers|jacket|watch|belt|accessory
- color_hex: dominant color as hex string (e.g. "#1A237E")
- color_name: human-readable color name (e.g. "Navy Blue")
- tags: array from office|casual|date_night|weekend|smart_casual|minimal|luxury|travel|festive|gym"""

USER_PROMPT = "Analyze this clothing item and return JSON with: suggested_name, category, color_hex, color_name, tags."

# ── Loaders ───────────────────────────────────────────────────────────────────

def load_synthetic(path: str = "data/synthetic_train.jsonl") -> list[dict]:
    """Load synthetic JSONL generated by generate_synthetic.py"""
    records = []
    p = Path(path)
    if not p.exists():
        print(f"  Synthetic dataset not found at {p}. Run: python data/generate_synthetic.py")
        return records
    with open(p) as f:
        for line in f:
            rec = json.loads(line.strip())
            records.append({
                "messages": [
                    {"role": "system",    "content": SYSTEM_PROMPT},
                    {"role": "user",      "content": rec["messages"][0]["content"]},
                    {"role": "assistant", "content": rec["messages"][1]["content"]},
                ],
                "source": "synthetic",
            })
    print(f"  ✓ Synthetic: {len(records)} records")
    return records


def load_deepfashion2(root: str) -> list[dict]:
    """
    Load DeepFashion2 annotations.
    Download: https://github.com/switchablenorms/DeepFashion2
    Expected structure: root/train/annos/*.json + root/train/image/*.jpg
    """
    records = []
    root_p  = Path(root)
    annos_p = root_p / "train" / "annos"
    imgs_p  = root_p / "train" / "image"

    if not annos_p.exists():
        print(f"  DeepFashion2 not found at {root}")
        return records

    import base64

    for anno_file in sorted(annos_p.glob("*.json"))[:5000]:  # cap at 5k for speed
        with open(anno_file) as f:
            anno = json.load(f)

        img_file = imgs_p / (anno_file.stem + ".jpg")
        if not img_file.exists():
            continue

        for item_key in anno:
            if not item_key.startswith("item"):
                continue
            item = anno[item_key]
            cat_id    = item.get("category_id", 0)
            cat_name  = DEEPFASHION_TO_WEARLY.get(cat_id, "accessory")
            cat_label = item.get("category_name", cat_name)

            # Encode image as base64
            with open(img_file, "rb") as img_f:
                img_b64 = base64.b64encode(img_f.read()).decode()

            output = {
                "suggested_name": cat_label.replace("_", " ").title(),
                "category": cat_name,
                "color_hex": "#808080",  # DeepFashion2 doesn't have colour annotations
                "color_name": "Unknown",
                "tags": ["casual"],
            }

            records.append({
                "messages": [
                    {"role": "system",    "content": SYSTEM_PROMPT},
                    {"role": "user",      "content": USER_PROMPT},
                    {"role": "assistant", "content": json.dumps(output)},
                ],
                "image_b64": img_b64,
                "source": "deepfashion2",
            })

    print(f"  ✓ DeepFashion2: {len(records)} records")
    return records


def load_imaterialist(annotations_file: str) -> list[dict]:
    """
    Load iMaterialist Fashion annotations.
    Download from Kaggle: imaterialist-fashion-2019-FGVC6
    annotations_file: path to train.json
    """
    records = []
    p = Path(annotations_file)
    if not p.exists():
        print(f"  iMaterialist not found at {annotations_file}")
        return records

    with open(p) as f:
        data = json.load(f)

    categories = {c["id"]: c["name"] for c in data.get("categories", [])}
    ann_by_img = {}
    for ann in data.get("annotations", []):
        img_id = ann["image_id"]
        ann_by_img.setdefault(img_id, []).append(ann)

    for img_info in data.get("images", [])[:3000]:  # cap at 3k
        img_id  = img_info["id"]
        if img_id not in ann_by_img:
            continue

        anns = ann_by_img[img_id]
        if not anns:
            continue

        # Take the most prominent annotation
        ann = anns[0]
        cat_name_raw = categories.get(ann.get("category_id", 0), "accessory").lower()
        cat_name     = IMATERIALIST_TO_WEARLY.get(cat_name_raw, "accessory")

        output = {
            "suggested_name": cat_name_raw.replace("_", " ").title(),
            "category": cat_name,
            "color_hex": "#808080",
            "color_name": "Unknown",
            "tags": ["casual"],
        }

        records.append({
            "messages": [
                {"role": "system",    "content": SYSTEM_PROMPT},
                {"role": "user",      "content": f"{USER_PROMPT}\n\n[Image URL: {img_info.get('url', '')}]"},
                {"role": "assistant", "content": json.dumps(output)},
            ],
            "source": "imaterialist",
        })

    print(f"  ✓ iMaterialist: {len(records)} records")
    return records


# ── Dataset builder ───────────────────────────────────────────────────────────

def build(
    deepfashion_root: Optional[str] = None,
    imaterialist_annotations: Optional[str] = None,
    output_dir: str = "data",
    val_split: float = 0.1,
) -> None:
    import random

    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)

    print("\nLoading datasets…")
    all_records: list[dict] = []

    # Always load synthetic
    all_records.extend(load_synthetic(str(out / "synthetic_train.jsonl")))

    # Optional external datasets
    if deepfashion_root:
        all_records.extend(load_deepfashion2(deepfashion_root))
    if imaterialist_annotations:
        all_records.extend(load_imaterialist(imaterialist_annotations))

    print(f"\nTotal records before split: {len(all_records)}")

    random.shuffle(all_records)
    n_val   = max(50, int(len(all_records) * val_split))
    val     = all_records[:n_val]
    train   = all_records[n_val:]

    print(f"Split → train: {len(train)}  val: {len(val)}")

    if HF_AVAILABLE:
        # Save as HuggingFace Dataset (Arrow format — fastest for training)
        def to_hf(records: list[dict]) -> Dataset:
            return Dataset.from_list([
                {"messages": r["messages"], "source": r.get("source", "unknown")}
                for r in records
            ])

        train_ds = to_hf(train)
        val_ds   = to_hf(val)

        train_ds.save_to_disk(str(out / "wearly_fashion_train"))
        val_ds.save_to_disk(str(out / "wearly_fashion_val"))
        print(f"\n✓ Saved HuggingFace Datasets to {out}/wearly_fashion_{{train,val}}/")
    else:
        # Fallback: save as JSONL
        with open(out / "prepared_train.jsonl", "w") as f:
            for r in train:
                f.write(json.dumps({"messages": r["messages"]}) + "\n")
        with open(out / "prepared_val.jsonl", "w") as f:
            for r in val:
                f.write(json.dumps({"messages": r["messages"]}) + "\n")
        print(f"\n✓ Saved JSONL to {out}/prepared_{{train,val}}.jsonl")

    # Print stats
    from collections import Counter
    sources = Counter(r.get("source", "unknown") for r in train)
    print("\nData sources (train):")
    for src, cnt in sorted(sources.items(), key=lambda x: -x[1]):
        print(f"  {src:20s} {cnt:5d}")


